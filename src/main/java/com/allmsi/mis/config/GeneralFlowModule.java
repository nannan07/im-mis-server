package com.allmsi.mis.config;

public class GeneralFlowModule {
	
	private String module;

	private Object flowInfo;
	
	private Object moduleInfo;
	
	private Object nodeDealList;
	
	public String getModule() {
		return module;
	}

	public void setModule(String module) {
		this.module = module;
	}

	public Object getFlowInfo() {
		return flowInfo;
	}

	public void setFlowInfo(Object flowInfo) {
		this.flowInfo = flowInfo;
	}

	public Object getModuleInfo() {
		return moduleInfo;
	}

	public void setModuleInfo(Object moduleInfo) {
		this.moduleInfo = moduleInfo;
	}

	public Object getNodeDealList() {
		return nodeDealList;
	}

	public void setNodeDealList(Object nodeDealList) {
		this.nodeDealList = nodeDealList;
	}
}
